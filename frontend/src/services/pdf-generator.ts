export async function generateTaxPDF(data: any, aiExplanation?: string): Promise<void> {
  try {
    const response = await fetch('/api/generate-report/tax', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(data),
    });
    if (!response.ok) throw new Error('API failed');
    const blob = await response.blob();
    safeDownloadPDF(blob, 'Finshala_Tax_Report.pdf');
    return;
  } catch (error) {
    console.warn("Python PDF API failed, falling back to local JS generation", error);
  }

  const { jsPDF } = await loadJsPDF();
  const doc = new jsPDF();

  addHeader(doc, 'Tax Wizard Report', `FY 2024-25 | AY 2025-26 | ${new Date().toLocaleDateString('en-IN')}`);
  let y = 35;

  y = addDisclaimer(doc, y);

  // Regime comparison
  y = addSection(doc, 'Regime Comparison', y);
  y = addRow(doc, 'Recommended Regime', data.recommended_regime.toUpperCase(), y, true);
  y = addRow(doc, 'Old Regime Tax', fmt(data.old_regime.total_tax), y);
  y = addRow(doc, 'New Regime Tax', fmt(data.new_regime.total_tax), y);
  y = addRow(doc, 'Annual Savings (by switching)', fmt(data.annual_savings || data.annual_savings_by_switching || 0), y, true);
  y = addRow(doc, 'Old Effective Rate', `${data.old_regime.effective_rate}%`, y);
  y = addRow(doc, 'New Effective Rate', `${data.new_regime.effective_rate}%`, y);
  y += 4;

  // Deductions (old regime)
  y = addSection(doc, 'Deductions Used (Old Regime)', y);
  data.old_regime.deduction_breakdown?.forEach((d: any) => {
    if (d.amount > 0) {
      y = addRow(doc, `Section ${d.section}`, `${fmt(d.amount)} / ${fmt(d.max)}`, y);
    }
  });
  y += 4;

  // Tax-saving moves
  if (data.tax_saving_moves?.length) {
    y = addSection(doc, 'Tax-Saving Opportunities', y);
    data.tax_saving_moves.forEach((m: any) => {
      y = addRow(doc, `${m.section}: ${m.action || m.description}`, `Save ${fmt(m.potential_saving || m.potential_tax_saving || 0)}`, y);
    });
    y += 4;
  }

  // Missed deductions
  if (data.missed_deductions?.length) {
    y = addSection(doc, 'Missed Deductions', y);
    data.missed_deductions.forEach((m: any) => {
      y = addRow(doc, `${m.section}: ${m.label}`, `Save up to ${fmt(m.potential_tax_saving)}`, y);
    });
    y += 4;
  }

  // AI explanation
  if (aiExplanation) {
    if (y > 220) { doc.addPage(); y = 20; }
    y = addSection(doc, 'AI Regime Analysis', y);
    y = addParagraph(doc, aiExplanation, y);
  }

  addFooter(doc);
  doc.save('Finshala_Tax_Report.pdf');
}

