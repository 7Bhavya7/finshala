// FIRE PLANNER PDF
// ═══════════════════════════════════════════════

export async function generateFirePDF(plan: any, aiRoadmap?: string): Promise<void> {
  try {
    const response = await fetch('/api/generate-report/fire', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(plan),
    });
    if (!response.ok) throw new Error('API failed');
    const blob = await response.blob();
    safeDownloadPDF(blob, 'Finshala_FIRE_Report.pdf');
    return;
  } catch (error) {
    console.warn("Python PDF API failed, falling back to local JS generation", error);
  }

  const { jsPDF } = await loadJsPDF();
  const doc = new jsPDF();
  const v = plan.variants[plan.preferred_variant];

  addHeader(doc, 'FIRE Planner Report', `Generated for Age ${plan.profile_summary.current_age} | ${new Date().toLocaleDateString('en-IN')}`);
  let y = 35;

  y = addDisclaimer(doc, y);

  // Summary
  y = addSection(doc, 'FIRE Summary', y);
  y = addRow(doc, 'Preferred Variant', plan.preferred_variant.toUpperCase() + ' FIRE', y, true);
  y = addRow(doc, 'FIRE Number', fmt(v.fire_number), y);
  y = addRow(doc, 'FIRE Age', `${v.fire_age} years old`, y);
  y = addRow(doc, 'Years to FIRE', `${v.years_to_fire} years`, y);
  y = addRow(doc, 'Current Net Worth', fmt(plan.current_net_worth), y);
  y = addRow(doc, 'Required Monthly SIP', fmt(v.required_monthly_sip), y);
  y = addRow(doc, 'Savings Rate Needed', `${v.savings_rate_needed}%`, y);
  y = addRow(doc, 'Feasibility', v.feasibility.toUpperCase(), y, true);
  y += 4;

  // All 3 variants comparison
  y = addSection(doc, 'Variant Comparison', y);
  (['lean', 'regular', 'fat'] as const).forEach((variant) => {
    const d = plan.variants[variant];
    y = addRow(doc, `${variant.toUpperCase()} FIRE`, `${fmt(d.fire_number)} by age ${d.fire_age} (${d.years_to_fire}yr) | SIP: ${fmt(d.required_monthly_sip)}/mo`, y);
  });
  y += 4;

  // Emergency Fund
  if (v.emergency_fund_plan) {
    y = addSection(doc, 'Emergency Fund', y);
    y = addRow(doc, 'Target Fund', fmt(v.emergency_fund_plan.target_fund), y);
    y = addRow(doc, 'Current Fund', fmt(v.emergency_fund_plan.current_fund), y);
    y = addRow(doc, 'Gap', fmt(v.emergency_fund_plan.gap), y);
    y = addRow(doc, 'Monthly Contribution', fmt(v.emergency_fund_plan.monthly_contribution), y);
    y += 4;
  }

  // Milestones
  if (v.milestones?.length) {
    y = addSection(doc, 'Key Milestones', y);
    v.milestones.slice(0, 8).forEach((m: any) => {
      y = addRow(doc, m.name, `${fmt(m.amount)} by ${m.target_date}`, y);
    });
    y += 4;
  }

  // SIP Allocations
  const sips = v.monthly_roadmap?.[0]?.sip_allocations;
  if (sips?.length) {
    y = addSection(doc, 'SIP Allocation Plan', y);
    sips.forEach((s: any) => {
      y = addRow(doc, `${s.goal_name} — ${s.fund_type.replace(/_/g, ' ')}`, fmt(s.amount), y);
    });
    y += 4;
  }

  // AI Roadmap
  if (aiRoadmap) {
    if (y > 220) { doc.addPage(); y = 20; }
    y = addSection(doc, 'AI-Generated Roadmap', y);
    y = addParagraph(doc, aiRoadmap, y);
  }

  addFooter(doc);
  doc.save('Finshala_FIRE_Report.pdf');
}

