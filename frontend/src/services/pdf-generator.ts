// MONEY HEALTH SCORE PDF
// ═══════════════════════════════════════════════

export async function generateHealthPDF(data: any, aiAdvice?: string): Promise<void> {
  try {
    const response = await fetch('/api/generate-report/health', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(data),
    });
    if (!response.ok) throw new Error('API failed');
    const blob = await response.blob();
    safeDownloadPDF(blob, 'Finshala_Health_Score_Report.pdf');
    return;
  } catch (error) {
    console.warn("Python PDF API failed, falling back to local JS generation", error);
  }

  const { jsPDF } = await loadJsPDF();
  const doc = new jsPDF();

  addHeader(doc, 'Money Health Score Report', `Score: ${data.overall_score}/900 (Grade ${data.overall_grade}) | ${new Date().toLocaleDateString('en-IN')}`);
  let y = 35;

  y = addDisclaimer(doc, y);

  // Overall
  y = addSection(doc, 'Overall Score', y);
  y = addRow(doc, 'Score', `${data.overall_score} / 900`, y, true);
  y = addRow(doc, 'Grade', data.overall_grade, y, true);
  y = addRow(doc, 'Status', (data.overall_status || 'Unknown'), y);
  y = addRow(doc, 'Monthly Income', fmt(data.meta?.monthly_income || 0), y);
  y = addRow(doc, 'Net Worth', fmt(data.meta?.net_worth || 0), y);
  y += 4;

  // Dimensions
  y = addSection(doc, '6-Dimension Breakdown', y);
  data.dimension_list?.forEach((dim: any) => {
    y = addRow(doc, `${dim.name}`, `${dim.score}/100 (Grade ${dim.grade})`, y);
    if (y > 260) { doc.addPage(); y = 20; }
  });
  y += 4;

  // Detailed Diagnostics
  if (data.dimension_list?.length) {
    y = addSection(doc, 'Detailed Diagnostics', y);
    data.dimension_list.forEach((dim: any) => {
      y = addRow(doc, dim.name, dim.insight || 'No specific finding.', y);
      if (y > 260) { doc.addPage(); y = 20; }
    });
    y += 4;
  }

  // Top Actions
  if (data.top_actions?.length) {
    y = addSection(doc, 'Priority Actions', y);
    data.top_actions.slice(0, 8).forEach((a: any, i: number) => {
      y = addRow(doc, `${i + 1}. [${a.priority?.toUpperCase() || ''}] ${a.dimension || ''}`, a.action, y);
      if (y > 260) { doc.addPage(); y = 20; }
    });
    y += 4;
  }

  // Strengths
  if (data.strengths?.length) {
    y = addSection(doc, 'Financial Strengths', y);
    data.strengths.forEach((s: any) => {
      const text = typeof s === 'string' ? s : `${s.dimension}: ${s.description || s.strength}`;
      y = addRow(doc, `✅ ${text}`, '', y);
      if (y > 260) { doc.addPage(); y = 20; }
    });
    y += 4;
  }

  // AI advice
  if (aiAdvice) {
    if (y > 220) { doc.addPage(); y = 20; }
    y = addSection(doc, 'AI Financial Advice', y);
    y = addParagraph(doc, aiAdvice, y);
  }

  addFooter(doc);
  doc.save('Finshala_Health_Score_Report.pdf');
}
