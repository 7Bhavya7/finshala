declare const jspdf: any;

function loadJsPDF(): Promise<any> {
  if ((window as any).jspdf) return Promise.resolve((window as any).jspdf);
  return new Promise((resolve, reject) => {
    const script = document.createElement('script');
    script.src = 'https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.2/jspdf.umd.min.js';
    script.onload = () => resolve((window as any).jspdf);
    script.onerror = () => reject(new Error('Failed to load jsPDF'));
    document.head.appendChild(script);
  });
}

// ── Safe PDF download helper ──
// Converts blob to base64 data-URI to avoid "unsafe" blob:http warnings
function safeDownloadPDF(blob: Blob, filename: string) {
  const reader = new FileReader();
  reader.onloadend = () => {
    const a = document.createElement('a');
    a.href = reader.result as string;
    a.download = filename;
    a.style.display = 'none';
    document.body.appendChild(a);
    a.click();
    // Small timeout to ensure download starts
    setTimeout(() => {
      document.body.removeChild(a);
    }, 200);
  };
  reader.readAsDataURL(blob);
}

// ── Shared helpers ──

function addHeader(doc: any, title: string, subtitle: string) {
  // Brand header
  doc.setFillColor(139, 111, 71); // Copper
  doc.rect(0, 0, 210, 28, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(18);
  doc.setFont('helvetica', 'bold');
  doc.text('FINSHALA', 14, 12);
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.text(title, 14, 20);
  doc.setFontSize(8);
  doc.text(subtitle, 14, 25);
  doc.setTextColor(0, 0, 0);
}

function addDisclaimer(doc: any, y: number): number {
  doc.setFillColor(255, 248, 230);
  doc.rect(14, y, 182, 14, 'F');
  doc.setDrawColor(184, 134, 11);
  doc.rect(14, y, 182, 14, 'S');
  doc.setFontSize(7);
  doc.setTextColor(120, 100, 50);
  doc.text('DISCLAIMER: This report is for educational purposes only. Not financial, tax, or investment advice.', 18, y + 5);
  doc.text('Consult a SEBI-registered advisor before making financial decisions.', 18, y + 10);
  doc.setTextColor(0, 0, 0);
  return y + 18;
}

function addSection(doc: any, title: string, y: number): number {
  if (y > 260) { doc.addPage(); y = 20; }
  doc.setFillColor(245, 240, 230);
  doc.rect(14, y, 182, 8, 'F');
  doc.setFontSize(10);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(139, 111, 71);
  doc.text(title, 18, y + 5.5);
  doc.setTextColor(0, 0, 0);
  doc.setFont('helvetica', 'normal');
  return y + 12;
}

function addRow(doc: any, label: string, value: string, y: number, bold = false): number {
  if (y > 270) { doc.addPage(); y = 20; }
  doc.setFontSize(9);
  doc.setFont('helvetica', bold ? 'bold' : 'normal');
  doc.text(label, 18, y);
  doc.text(value, 180, y, { align: 'right' });
  return y + 5.5;
}

function addParagraph(doc: any, text: string, y: number, maxWidth = 170): number {
  if (y > 260) { doc.addPage(); y = 20; }
  doc.setFontSize(8);
  doc.setFont('helvetica', 'normal');
  const lines = doc.splitTextToSize(text, maxWidth);
  doc.text(lines, 18, y);
  return y + lines.length * 4 + 2;
}

function addFooter(doc: any) {
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(7);
    doc.setTextColor(150, 150, 150);
    doc.text(`Page ${i} of ${pageCount} | Generated by Finshala AI | ${new Date().toLocaleDateString('en-IN')}`, 105, 290, { align: 'center' });
  }
}

const fmt = (n: number) => n >= 10000000 ? `Rs ${(n/10000000).toFixed(2)}Cr` : n >= 100000 ? `Rs ${(n/100000).toFixed(1)}L` : `Rs ${(n/1000).toFixed(0)}K`;

